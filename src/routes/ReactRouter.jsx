import { Routes, Route } from 'react-router-dom'
import { routes } from './routes'

const ReactRouter = () => {
    return (
        <Routes>
            {Object.keys(routes).map((route) => (
                <Route
                    key={route}
                    path={routes[route].path}
                    element={routes[route].getPage()}
                />
            ))}
        </Routes>
    )
}

export default ReactRouter
